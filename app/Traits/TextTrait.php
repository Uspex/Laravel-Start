    /**
     * Очистим текст от лишних символов
     * @param $string
     * @param $add_symbol - список допустимых символов
     * @return string|string[]|null
     */
    public static function cleanSearchString($string, $add_symbol = '') {
        return preg_replace('/[^a-zA-Zа-яА-Я0-9'.$add_symbol.']/ui', '',$string );
    }


    /**
     * Преобразуем массив в строку с разделителем
     * @param $data
     * @param string $delimited
     * @return string
     */
    public static function getArrayToString($data, $delimited = ':'){
        $result = '';

        $count = count($data)-1;
        $i = 1;

        foreach ($data as $item){
            $result .= $item;

            if($i <= $count){
                $result .= $delimited;
            }
            $i++;
        }

        return $result;

    }

    /**
     * Очистим строку, оставим только цифры
     * @param $string
     * @return string|string[]|null
     */
    public static function getOnlyNumbers($string){
        return preg_replace('/[^0-9]/ui', '',$string );
    }
}
